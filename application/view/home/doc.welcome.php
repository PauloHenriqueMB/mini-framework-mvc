	  					<div class="page-header-doc"><!-- page-header-doc -->
					      	<h4>Introduction</h4>
					    </div><!-- page-header-doc -->
					    <p class="text-justify">
							<strong>MINI-FRAMEWORK-MVC</strong> is an extremely simple and easy to understand PHP framework based on project <a href="https://github.com/panique/mini3">MINI3</a>.
							<strong>MINI-FRAMEWORK-MVC</strong> is NOT a professional framework. As a result, it does not come with all the features and functionalities that real 
							frameworks have. It is limited to a very reduce number of helper libraries.
							<br></br>
							This project can be suitable to anybody who just wants to show some pages, make some database calls, implement sessions, validate forms, 
							and do some AJAX calls here and there, without the need of reading a lot of documentation of those more advance, complex and professional 
							frameworks. <strong>MINI-FRAMEWORK-MVC</strong> is easy to install, runs nearly everywhere and doesn't make things more complicated than necessary.
							<br></br>
							For a deeper introduction into <strong>MINI-FRAMEWORK-MVC</strong> root project <a href="https://github.com/panique/mini3">MINI3</a>, have a look into this blog post:
							<a href="http://www.dev-metal.com/mini-extremely-simple-barebone-php-application/">MINI, an extremely simple barebone PHP application</a>.
							<br></br>
							We really hope this small "framework" can be useful as <a href="https://github.com/panique/mini3">MINI3</a> has been to us. If you would like to have a look 
							into our other projects, please visit us at <a href="http://www.canchito-dev.com">Canchito-Dev</a>.
						</p>
					</section><!-- introduction -->
					
