	  					<div class="page-header-doc"><!-- page-header-doc -->
					      	<h4>History</h4>
					    </div><!-- page-header-doc -->
					    <p class="text-justify">
							<strong>MINI-FRAMEWORK-MVC</strong> started as an idea and a challenge. I am not exactly a professional PHP programmer, but I had the 
							basic knowledge. However, I wanted to improve and learn in dept how a MVC framework was build. So, I bought the book <strong>Pro PHP MVC</strong> by 
							<strong>Chris Pitt</strong>, and read it during my free time. In this book, I got the chance to get to know current most popular professional MVC
							frameworks. And at the same time, the book explains how to build from scratch classes that are useful for a MVC Framework.
							<br></br>
							To complement the book, I also took the Udemy's course 
							<a href="https://www.udemy.com/learn-php-model-view-controller-pattern-php-mvc/learn/v4/overview">Learn PHP Model View Controller Pattern (PHP MVC)</a>. 
							It was in this course where I understood how to join the different classes need in a framework and got to know <a href="https://github.com/panique/mini3">MINI3</a>.
							<br></br>
							After reading the book and finishing the course, I though myself it would be a good idea to combine some of the lessons learnt from both and implement 
							them in <a href="https://github.com/panique/mini3">MINI3</a>. But I did not want to build everything from scratch. And that is why, some of the libraries 
							are just implementations of already existing ones, whereas some are adaptations of snippets of code, and some others are fully developed by me.
						</p>
